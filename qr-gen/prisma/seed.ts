async function main() {
  console.log(`Start seeding ...`)
  for (const dataUrl of qrData) {
    const qr = await prisma.qrCode.create({
      data: dataUrl,
    })
    // console.log(`Created Qrcode data: ${qr.id}`)
    return qr.id
  }
  console.log(`Seeding finished.`)
}


